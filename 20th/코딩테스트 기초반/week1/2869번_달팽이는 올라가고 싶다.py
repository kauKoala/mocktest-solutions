a, b, v = map(int, input().split())

# 마지막 날에는 오르기만 하고 미끄러지지 않으므로, 마지막 오르기를 제외한 이전 날들에서 올라야 할 높이는 -> (v - a)
# 하루에 실제로 올라가는 높이는 -> (a - b)
# (v - a) 를 하루에 (a - b) 씩 올라간다고 보고, ((v - a) / (a - b))를 올림 처리 해야하므로
# ((v - a) / (a - b)) == (v - a + (a - b) - 1) // (a - b)
# 전날까지 (v - a)만 도달했으니, 다음날 마지막으로 a 올라가고 끝나야 하므로 + 1 해줌
day = (v - a + (a - b) - 1) // (a - b) + 1

print(day)