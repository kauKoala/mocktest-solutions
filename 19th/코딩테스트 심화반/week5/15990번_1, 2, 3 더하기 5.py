import sys
input = sys.stdin.readline

MOD = 1_000_000_009
MAX = 100_001

# dp[i][j]: 정수 i를 j로 끝나는 수열로 만드는 방법의 수
# j는 마지막에 더한 수 (1, 2, 3 중 하나)
dp = [[0] * 4 for _ in range(MAX)]

# 초기 조건 설정
dp[1][1] = 1  # 1을 만드는 방법: [1]
dp[2][2] = 1  # 2를 만드는 방법: [2]
dp[3][1] = 1  # 3을 만드는 방법: [2+1]
dp[3][2] = 1  # 3을 만드는 방법: [1+2]
dp[3][3] = 1  # 3을 만드는 방법: [3]

for i in range(4, MAX):
    # i를 1로 끝나는 수열로 만들려면, 직전에 2 또는 3이 와야 한다
    dp[i][1] = (dp[i-1][2] + dp[i-1][3]) % MOD

    # i를 2로 끝나는 수열로 만들려면, 직전에 1 또는 3이 와야 한다
    dp[i][2] = (dp[i-2][1] + dp[i-2][3]) % MOD

    # i를 3으로 끝나는 수열로 만들려면, 직전에 1 또는 2가 와야 한다
    dp[i][3] = (dp[i-3][1] + dp[i-3][2]) % MOD

for _ in range(int(input())):
    n = int(input())
    # n을 만들 수 있는 총 경우의 수 = [마지막 수가 1인 경우] + [2인 경우] + [3인 경우]
    print((dp[n][1] + dp[n][2] + dp[n][3]) % MOD)